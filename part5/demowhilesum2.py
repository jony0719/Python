'''计算1-100之间偶数的和'''
a = 1
sum = 0
while a <= 100:
    '''条件执行体（循环体）'''
    if not bool(a % 2):  # bool(a%2)为True,表示奇数；为False，表示为偶数
        sum += a
    a += 1
print('1-100的偶数和:', sum)
