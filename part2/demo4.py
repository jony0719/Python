# 整数类型
# 可以表示为正数、负数、零
n1 = 76
n2 = -87
n3 = 0
print('正数', n1)
print('负数', n2)
print('零', n3)
print(n1, type(n1))
print(n2, type(n2))
print(n3, type(n3))
# 整数可以表示为，十进制、二进制、八进制、十六进制，默认表示为十进制
print('十进制', 118)  # 默认十进制
print('二进制', 0b10100101)  # 0b二进制
print('八进制', 0O213)  # 0o表示八进制
print('十六进制', 0x3af)  # 0x表示十六进制
