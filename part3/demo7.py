print('============按位与按位或====================')
# 4=00000100
# 8=00001000
print(4 & 8)  # 00000000，结果-》0
print(4 | 8)  # 00001100，结果-》10

print('=============左移位与右移位===================')
print(4 << 1)  # 向左移动1个位置，相当于乘以2
print(4 << 2)  # 向左移动2个位置，相当于乘以4

print(4 > 1)  # 向右移动1个位置，相当于除以2
print(4 > 2)  # 向右移动2个位置，相当于除以4
